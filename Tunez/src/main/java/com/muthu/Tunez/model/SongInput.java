package com.muthu.Tunez.model;

public class SongInput {
    
    private String block;
    private String song;

    public SongInput(){}

    public String getBlock() {
        return block;
    }

    public void setBlock(String block) {
        this.block = block;
    }

    public String getSong() {
        return song;
    }

    public void setSong(String artist) {
        this.song = artist;
    }

    

}
